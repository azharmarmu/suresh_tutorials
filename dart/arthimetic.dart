void main() {
  int a = 30;
  int b = 20;


  //Addition +
  int sum = a + b; // = 50

  print('sum: $sum'); //string interpolation

  //Subraction -
  int minus = a - b;
  print('sub: $minus');

  //Multiplication *
  int mul = a * b;
  print('mul: $mul');

  //Division /
  num div = a / b;
  print('div: $div');

  //Modulus %
  int reminder = a % b;
  print('Mod: $reminder');
}
